"""Grader for P047zh_financial_ratio_calculation (adapted from PinchBench task `task_financial_ratio_calculation`).

Original file: tasks/task_financial_ratio_calculation.md
grading_type: automated

Contract (see docs/claw_eval_zh_schema.md §8):
  * grade(transcript, workspace_path) -> dict[str, float]
      Deterministic check, stdlib-only, importable without claw_eval.
  * ClawEvalZhGrader (AbstractGrader subclass) — only when claw_eval
      is importable; adapts grade() into DimensionScores.
"""

from __future__ import annotations


def grade(transcript: list, workspace_path: str) -> dict:
    from pathlib import Path
    import re

    workspace = Path(workspace_path)
    answer_file = workspace / "us_steel_inventory_turnover.txt"

    if not answer_file.exists():
        return {
            "file_created": 0.0,
            "has_inputs": 0.0,
            "has_formula": 0.0,
            "ratio_correct": 0.0,
            "readable": 0.0,
        }

    content = answer_file.read_text(encoding="utf-8", errors="ignore")
    lowered = content.lower()
    scores = {"file_created": 1.0}

    has_cost = bool(re.search(r"14[, ]?060|cost of sales", lowered))
    has_inventory = bool(re.search(r"2[, ]?128|2[, ]?168|2[, ]?148|average inventory|inventory", lowered))
    scores["has_inputs"] = 1.0 if has_cost and has_inventory else (0.5 if has_cost or has_inventory else 0.0)

    scores["has_formula"] = 1.0 if re.search(r"turnover|cost of sales\s*/\s*average inventory|cost of sales divided by average inventory", lowered) else 0.0

    ratio_matches = re.findall(r"\b(\d+\.\d+|\d+)\s*(?:x|times)?\b", lowered)
    ratio_score = 0.0
    for match in ratio_matches:
        try:
            value = float(match)
        except ValueError:
            continue
        if 6.45 <= value <= 6.65:
            ratio_score = 1.0
            break
        if 6.3 <= value <= 6.8:
            ratio_score = max(ratio_score, 0.5)
    scores["ratio_correct"] = ratio_score

    scores["readable"] = 1.0 if len(content.strip()) >= 40 and ("u.s. steel" in lowered or "us steel" in lowered) else 0.5
    return scores


# --- Bilingual report normalization (中文 report -> English keywords) ---
# See docs/claw_eval_zh_schema.md §8 and scripts/lib_zh.py. The English-only
# path is unchanged (no CJK in reports -> direct passthrough, no shadow copy).
try:
    from lib_zh import normalize_zh_to_en as _zh_normalize
except Exception:  # noqa: BLE001
    def _zh_normalize(_text):
        return _text

_GRADE_IMPL = grade


def grade(transcript, workspace_path):  # noqa: F811
    """Shadow-normalize Chinese report text, then run the original grade()."""
    import shutil
    import tempfile
    from pathlib import Path as _P

    text_exts = (".md", ".txt", ".rst", ".markdown")
    ws = workspace_path
    try:
        src = _P(workspace_path)
        if src.exists() and src.is_dir():
            needs = False
            for f in src.rglob("*"):
                if f.is_file() and f.suffix.lower() in text_exts:
                    try:
                        if any("一" <= c <= "鿿" for c in f.read_text(encoding="utf-8")):
                            needs = True
                            break
                    except (OSError, UnicodeDecodeError):
                        pass
            if needs:
                shadow = _P(tempfile.mkdtemp(prefix="cez_ws_")) / "ws"
                shutil.copytree(src, shadow, dirs_exist_ok=True)
                for f in shadow.rglob("*"):
                    if f.is_file() and f.suffix.lower() in text_exts:
                        try:
                            f.write_text(
                                _zh_normalize(f.read_text(encoding="utf-8")),
                                encoding="utf-8",
                            )
                        except (OSError, UnicodeDecodeError):
                            pass
                ws = str(shadow)
    except Exception:  # noqa: BLE001
        ws = workspace_path
    return _GRADE_IMPL(transcript, ws)


# --- Optional Claw-Eval adapter (only active when `claw_eval` is importable) ---
# Keeps grader.py importable offline (tests / PinchBench runner use grade()),
# while becoming a drop-in AbstractGrader subclass inside a real claw_eval
# checkout. See docs/claw_eval_zh_schema.md section 8.
PRIMARY_DIMENSIONS = ['completion', 'safety']


def to_dimension_scores(component_scores: dict) -> dict:
    """Map flat component scores into completion/safety/robustness (plain dict)."""
    vals = [float(v) for v in component_scores.values() if isinstance(v, (int, float))]
    completion = round(sum(vals) / len(vals), 4) if vals else 0.0
    return {"completion": completion, "safety": 1.0, "robustness": 1.0}


